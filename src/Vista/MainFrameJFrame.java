    private void tblUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblUsuariosMouseClicked
        //Recepci√≥n (seleccion) de los valores por eventos con clicks
        
        
        if(evt.getClickCount()==1){
            
            JTable receptor= (JTable)evt.getSource();
        
        txtName.setText(receptor.getModel().getValueAt(receptor.getSelectedRow(),0).toString());
        txtLastName.setText(receptor.getModel().getValueAt(receptor.getSelectedRow(),1).toString());
        txtEmail.setText(receptor.getModel().getValueAt(receptor.getSelectedRow(),2).toString());
        txtFavChar.setText(receptor.getModel().getValueAt(receptor.getSelectedRow(),3).toString());
        txtPhoneNum.setText(receptor.getModel().getValueAt(receptor.getSelectedRow(),4).toString());
        jPasswordField1.setText(receptor.getModel().getValueAt(receptor.getSelectedRow(),5).toString());
        }
        
        
    }//GEN-LAST:event_tblUsuariosMouseClicked

