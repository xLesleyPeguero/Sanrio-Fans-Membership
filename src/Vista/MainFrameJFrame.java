    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed

         DbConnection objConnect = new DbConnection();
        
        objConnect.establishConn();
        
        Usuarios objUsuarios= retrievedataGUI();
        //Recuperar datos GUI
        String strSentenceInsert = String.format(
    "UPDATE users SET first_name='%s', last_name='%s', email='%s', password='%s', favorite_character='%s',"
            + " phone_number='%s' WHERE email='%s'",
    objUsuarios.getName(),
    objUsuarios.getLastName(),
    objUsuarios.getEmail(),
    objUsuarios.getPassword(),
    objUsuarios.getFavChar(),
    objUsuarios.getPhoneNum(),
    objUsuarios.getEmail());
                
        objConnect.executeSQL(strSentenceInsert);
        
        this.ShowData();
        this.clear();
       
        
    }//GEN-LAST:event_btnUpdateActionPerformed
  //M√©todo para cargar/actualizar los datos
    
    public void ShowData(){
        while(modelo.getRowCount()>0){
            modelo.removeRow(0);
        
        
        }
    DbConnection objConnect = new DbConnection();
    objConnect.establishConn();    
    
        try {
            ResultSet resultado= objConnect.consultarRegistros("SELECT * FROM users");
            
            while (resultado.next()) {

                
                Object[] oUsuario= {resultado.getString("first_name"), resultado.getString("last_name"),
                    resultado.getString("email"), resultado.getString("favorite_character"),resultado.getString("phone_number")};
                
                modelo.addRow(oUsuario);

            }
        } catch (Exception e) {
            
            System.out.println(e);
        }
        

            
    }
